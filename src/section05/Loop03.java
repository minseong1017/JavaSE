package section05;
/*
 * 3. for문
 * 	초기식, 조건식, 증감식을 이용하여 반복적으로 실행하는 명령어
 * 
 * 	for(초기식; 조건식; 증감식){
 * 		반복 수행할 명령어
 * }
 * 
 */
public class Loop03 {
	public static void main(String[] args) {
		for(int i=1; i<=10; i++)
		{
			System.out.println(i);
			}
		
	
	int j = 1;
	while (j<=10) {
		System.out.println(j);
		j++;
	}
	}
}
